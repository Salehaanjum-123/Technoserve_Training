package technoserve;

public class incrementOperator {

	public static void main(String[] args) {
		int a=10;
		
		//post inc
		System.out.println("a++ "+ a++);
		System.out.println("after post inc of a "+a);
		
		//pre inc
		System.out.println("++a "+ ++a);
		
		//post dec
				System.out.println("a-- "+ a--);
				System.out.println("after post dec of a "+a);
				
				//pre inc
				System.out.println("--a "+ --a);
	}

}
