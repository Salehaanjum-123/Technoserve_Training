package technoserve;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a=10;
		
		//add and assign
		a+=5;
		System.out.println("a after += "+a);//15
		
		//add and assign
		a-=3;
		System.out.println("a after -= "+a);
		
		//multiply and assign
		a *=2;
		System.out.println("a after *= "+a); 
		
		//divide and assign
		a/=4;
		System.out.println("a after /= "+a);
		
		//mod and assign
		a%=3;
		System.out.println("a after %= "+a);
				
				
				
				
	}

}
